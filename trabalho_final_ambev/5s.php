    <h3>Seiri (Classificação):</h3>

        <p>Refere-se à organização e à identificação dos itens essenciais no local de trabalho.
            Durante essa etapa, é feita uma seleção criteriosa de materiais e ferramentas,
            separando o necessário do desnecessário. Itens não essenciais são removidos,
            liberando espaço e facilitando o acesso ao que é realmente importante.
        </p><br><br>

    <h3>Seiton (Ordenação):</h3>
        <p>
            Envolve a definição de um lugar específico para cada item essencial selecionado na etapa anterior.
            Os objetos são organizados de forma lógica e de fácil acesso,
            com o objetivo de melhorar a eficiência e evitar desperdícios de tempo na busca por materiais ou informações.

        </p><br><br>

    <h3>Seiso (Limpeza):</h3>
        <p>
            Trata-se da limpeza e manutenção regular do ambiente de trabalho.
            É importante criar um hábito de limpeza diária para evitar o acúmulo de sujeira,
            prevenir a deterioração de equipamentos e garantir a segurança dos funcionários.
            Todos os membros da equipe são responsáveis pela manutenção da limpeza.
        </p><br><br>

    <h3>Seiketsu (Padronização):</h3>
        <p>
            Nessa etapa, são estabelecidas regras e procedimentos padrão para manter as práticas de classificação,
            ordenação e limpeza adotadas anteriormente. A ideia é criar um ambiente de trabalho padronizado,
            onde todos os funcionários seguem as mesmas diretrizes,
            facilitando a comunicação, a colaboração e a manutenção dos padrões.
        </p><br><br>

    <h3>Shitsuke (Disciplina):</h3>
        <p>
        A última etapa do 5S enfoca a disciplina e a manutenção dos padrões estabelecidos.
         É necessário criar uma cultura de responsabilidade, em que todos se comprometam a seguir as práticas implementadas.
         Isso envolve treinamento, monitoramento e incentivos para garantir a aderência contínua aos princípios do 5S.
        </p><br><br>

